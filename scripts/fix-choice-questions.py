def fix():
    path = 'downloads/questions-AI-Native-Startup.json'
    with open(path, 'r', encoding='utf-8') as f:
        qs = json.load(f)
    real = [q for q in qs if q.get('question') and not q.get('_instructions')]
    inst = [q for q in qs if q.get('_instructions')]
    fixed_q = fixed_e = 0
    for q in real:
        if q['id'] in FIXES_Q:
            q['question'] = FIXES_Q[q['id']]; fixed_q += 1
        if q['id'] in FIXES_E:
            q['extension'] = FIXES_E[q['id']]; fixed_e += 1
    json.dump(inst + real, open(path, 'w', encoding='utf-8'), ensure_ascii=False, indent=2)
    print(f'Fixed Q:{fixed_q} E:{fixed_e}')

if __name__ == '__main__':
    fix()
