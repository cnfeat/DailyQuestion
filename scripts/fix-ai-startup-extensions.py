def fix():
    path = 'downloads/questions-AI-Native-Startup.json'
    with open(path, 'r', encoding='utf-8') as f:
        qs = json.load(f)
    real = [q for q in qs if q.get('question') and not q.get('_instructions')]
    inst = [q for q in qs if q.get('_instructions')]
    fixed = 0
    for q in real:
        if q['id'] in FIXES:
            q['extension'] = FIXES[q['id']]
            fixed += 1
    json.dump(inst + real, open(path, 'w', encoding='utf-8'), ensure_ascii=False, indent=2)
    lens = [len(q['extension']) for q in real]
    over = [q for q in real if len(q['extension']) > 110]
    print(f'Fixed: {fixed} | Avg: {sum(lens)/len(lens):.0f}字 | >110: {len(over)}')

if __name__ == '__main__':
    fix()
