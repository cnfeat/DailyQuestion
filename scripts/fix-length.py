def apply_fixes(filepath):
    with open(filepath, 'r', encoding='utf-8') as f:
        questions = json.load(f)
    
    real_qs = [q for q in questions if q.get('question') and not q.get('_instructions')]
    instructions = [q for q in questions if q.get('_instructions')]
    
    fixed_q = 0
    fixed_e = 0
    
    for q in real_qs:
        qid = q['id']
        if qid in FIXES_Q:
            old_len = len(q['question'])
            q['question'] = FIXES_Q[qid]
            fixed_q += 1
        if qid in FIXES_E:
            old_len = len(q['extension'])
            q['extension'] = FIXES_E[qid]
            fixed_e += 1
    
    result = instructions + real_qs
    with open(filepath, 'w', encoding='utf-8') as f:
        json.dump(result, f, ensure_ascii=False, indent=2)
    
    print(f'已修复: {fixed_q} 个主问题 + {fixed_e} 个 extension')

if __name__ == '__main__':
    import sys
    path = sys.argv[1] if len(sys.argv) > 1 else 'questions.json'
    apply_fixes(path)
