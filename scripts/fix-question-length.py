def fix():
    with open('questions.json', 'r', encoding='utf-8') as f:
        qs = json.load(f)
    real = [q for q in qs if q.get('question') and not q.get('_instructions')]
    inst = [q for q in qs if q.get('_instructions')]
    fixed = 0
    for q in real:
        if q['id'] in FIXES_Q:
            old = q['question']
            q['question'] = FIXES_Q[q['id']]
            fixed += 1
    json.dump(inst + real, open('questions.json', 'w', encoding='utf-8'), ensure_ascii=False, indent=2)
    
    # verify
    short = [q for q in real if len(q['question']) < 28]
    print(f'Fixed: {fixed}  |  Still <28: {len(short)}')
    for q in short[:10]:
        print(f'  [{q["id"]}] {len(q["question"])}: {q["question"][:50]}')

if __name__ == '__main__':
    fix()
